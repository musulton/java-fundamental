public class IfElse {
    public static void main(String[] args) {
        boolean mtk = true;
        boolean ipa = false;

        if (mtk) {
            System.out.println("Anda masuk jurusna matematika");
        } else if (ipa) {
            System.out.println("Anda masuk jurusan ipa");
        } else {
            System.out.println("Maaf anda belum lulus");
        }
    }
}
